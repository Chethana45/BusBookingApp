// Export bus by ID
export const getBusById = (id) => {
  return buses.find((bus) => bus.id === id);
};

// Export buses by route
export const getBusesByRoute = (from, to) => {
  return buses.filter((bus) => bus.from.toLowerCase() === from.toLowerCase() && bus.to.toLowerCase() === to.toLowerCase());
};

// Export available buses
export const getAvailableBuses = () => {
  return buses.filter((bus) => bus.availableSeats > 0);
};

// Export buses by type
export const getBusesByType = (type) => {
  return buses.filter((bus) => bus.busType.toLowerCase() === type.toLowerCase());
};

